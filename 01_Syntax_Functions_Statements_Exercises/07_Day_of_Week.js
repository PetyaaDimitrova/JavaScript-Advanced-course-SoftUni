function dayOfWeek(input){
    if(input == "Monday"){
        console.log(1);
    } else if(input == "Tuesday"){
        console.log(2);
    }else if(input == "Wednesday"){
        console.log(3);
    }else if(input == "Thuesday"){
        console.log(4);
    }else if(input == "Friday"){
        console.log(5); 
    } else {
        console.log("Error");
    }
}

dayOfWeek("Friday");